function SidebarLink({ href, label, icon }: { href: string; label: string; icon: string }) {
  const pathname = usePathname()
  const isActive = href === '/portal/dashboard'
    ? pathname === '/portal/dashboard'
    : pathname.startsWith(href)

  return (
    <Link
      href={href}
      className={cn(
        'flex items-center gap-2.5 px-3 py-2 rounded-lg text-sm transition-colors',
        isActive
          ? 'bg-tps-orange/10 text-tps-orange font-semibold'
          : 'text-gray-600 hover:text-tps-navy hover:bg-gray-100',
      )}
    >
      <span className="shrink-0">{ICONS[icon] ?? ICONS.home}</span>
      {label}
    </Link>
  )
}

export function PortalSidebar({ role }: { role: UserRole }) {
  const [open, setOpen] = useState(false)

  // Invisible Access Control — only render nav items the user can access
  const visibleItems = PORTAL_NAV.filter(item =>
    can(role, item.requiredPermission as Permission),
  )

  const nav = (
    <nav className="space-y-0.5 p-3">
      {visibleItems.map(item => (
        <SidebarLink key={item.href} href={item.href} label={item.label} icon={item.icon} />
      ))}

      {/* Grading Tools divider — link back to existing admin grading portal */}
      <div className="pt-3 mt-3 border-t border-gray-200">
        <p className="px-3 mb-1 text-[10px] font-bold uppercase tracking-widest text-gray-400">
          Grading Tools
        </p>
        <SidebarLink href="/admin" label="Grading Portal" icon="chart-bar" />
      </div>

      {/* Chat stub */}
      <div className="pt-3 mt-3 border-t border-gray-200">
        <p className="px-3 mb-1 text-[10px] font-bold uppercase tracking-widest text-gray-400">
          Digital Squadron
        </p>
        <SidebarLink href="/portal/chat" label="Chat" icon="chat" />
      </div>
    </nav>
  )

  return (
    <>
      {/* Mobile toggle */}
      <button
        className="md:hidden fixed bottom-4 right-4 z-50 bg-tps-navy text-white px-4 py-2 rounded-full shadow-lg text-sm font-medium"
        onClick={() => setOpen(!open)}
        aria-label="Toggle navigation"
      >
        {open ? 'Close' : 'Menu'}
      </button>

      {/* Mobile overlay */}
      {open && (
        <div
          className="md:hidden fixed inset-0 z-40 bg-black/40"
          onClick={() => setOpen(false)}
        />
      )}

      {/* Sidebar */}
      <aside
        className={cn(
          'fixed top-14 left-0 h-[calc(100vh-3.5rem)] w-52 bg-white border-r border-gray-200 overflow-y-auto z-40 transition-transform',
          open ? 'translate-x-0' : '-translate-x-full md:translate-x-0',
        )}
        onClick={() => setOpen(false)}
      >
        {nav}
      </aside>
    </>
  )
}
